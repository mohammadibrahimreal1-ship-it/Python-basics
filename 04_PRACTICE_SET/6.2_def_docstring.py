def multiply(a,b):
    """
    Returns the multiplication of two numbers.

    Parameters:
    a (int): The first number.
    b (int): The second number.

    Returns:
    int: The multiplication of the two numbers.
    """
    return a * b

print(multiply(5,3))
print(multiply(2.5, 4))

